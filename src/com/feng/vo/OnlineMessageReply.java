package com.feng.vo;

public class OnlineMessageReply {
	private int id;
	private int rpmsgId;
	private String rpmsg;
	private String rptime;
	private String rpuname;
	private String rpimagePath;
	private int rpuid;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getRpmsgId() {
		return rpmsgId;
	}
	public void setRpmsgId(int rpmsgId) {
		this.rpmsgId = rpmsgId;
	}
	public String getRpmsg() {
		return rpmsg;
	}
	public void setRpmsg(String rpmsg) {
		this.rpmsg = rpmsg;
	}
	public String getRptime() {
		return rptime;
	}
	public void setRptime(String rptime) {
		this.rptime = rptime;
	}
	public String getRpuname() {
		return rpuname;
	}
	public void setRpuname(String rpuname) {
		this.rpuname = rpuname;
	}
	public String getRpimagePath() {
		return rpimagePath;
	}
	public void setRpimagePath(String rpimagePath) {
		this.rpimagePath = rpimagePath;
	}
	public int getRpuid() {
		return rpuid;
	}
	public void setRpuid(int rpuid) {
		this.rpuid = rpuid;
	}
	
	
}
